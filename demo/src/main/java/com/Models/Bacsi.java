package com.Models;

public class Bacsi {
    private int  id_bacsi;
    private String ten;
    private int tuoi;
    private String diaChi;
    private String sdt;
    private String email;
    private String gioiTinh;
    private int soNamKinhNghiem;
    private String chyenMon;
    public int getId_bacsi() {
        return id_bacsi;
    }
    public void setId_bacsi(int id_bacsi) {
        this.id_bacsi = id_bacsi;
    }
    public String getTen() {
        return ten;
    }
    public void setTen(String ten) {
        this.ten = ten;
    }
    public int getTuoi() {
        return tuoi;
    }
    public void setTuoi(int tuoi) {
        this.tuoi = tuoi;
    }
    public String getDiaChi() {
        return diaChi;
    }
    public void setDiaChi(String diaChi) {
        this.diaChi = diaChi;
    }
    public String getSdt() {
        return sdt;
    }
    public void setSdt(String sdt) {
        this.sdt = sdt;
    }
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public String getGioiTinh() {
        return gioiTinh;
    }
    public void setGioiTinh(String gioiTinh) {
        this.gioiTinh = gioiTinh;
    }
    public int getSoNamKinhNghiem() {
        return soNamKinhNghiem;
    }
    public void setSoNamKinhNghiem(int soNamKinhNghiem) {
        this.soNamKinhNghiem = soNamKinhNghiem;
    }
    public String getChyenMon() {
        return chyenMon;
    }
    public void setChyenMon(String chyenMon) {
        this.chyenMon = chyenMon;
    }
    
}
